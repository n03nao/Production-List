package circle;

abstract public class AbstractCircle {

	/**
	 * abstract Class of Circle
	 * @author T.Nozawa
	 */

		//Area of Circle
		abstract public double getArea();
		//Circumference
		abstract public double getCircumference();
		//get X Position
		abstract public double getXPos();
		//get y Position
		abstract public double getYPos();

}